public class CaseSwitch{
    public static void main(String[] args){
            String color = "blue";
            switch(color) {
         case   "blue":
                System.out.println(" My faforit color is blue");
                break;
            case "red":
                System.out.println("  My faforit color is red");
                break;
            case "green":
                System.out.println("  My faforit color is green");
                break;
           default:
                System.out.println("  My faforit color is black");
            }
    }
}


